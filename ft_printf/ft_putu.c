#include "ft_printf.h"

int	ft_putu(unsigned int nb)
{
	int	i;

	i = 0;
	if (nb >= 10)
	{
		i += ft_putu(nb / 10);
		i += ft_putu(nb % 10);
	}
	if (nb < 10)
		i += ft_putc (nb + 48);
	return (i);
}
